tuple = ("a", "b", "c", "d")
newTuple = (1, 2, 3, 4, 5, 1)

print(tuple + newTuple)                  # concatenation

print(tuple * 4)                         # repeats 4 times

print(4 in newTuple)                     # in operator

print(newTuple.count(1))

print(newTuple.index(5))

print(len(newTuple))                     # Length of tuple

print(max(tuple))                          # max element

print(min(newTuple))                          # min element




